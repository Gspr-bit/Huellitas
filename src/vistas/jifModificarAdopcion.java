    private String validarCajas() {
        String resultado = "true";
        if (isCambioComprobante()) {
            if (rutaComprobante == null) {
                resultado = "Sube un comprobante";
                btnComprobante.requestFocus();
            }
        }
        if (isCambioCredencial()) {
            if (rutaCredencial == null) {
                resultado = "Sube una credencial";
                btnCredencial.requestFocus();
            } 
        }        
        
        if (!validar.noVacio(txtFolio.getText())) {
            resultado = "Faltan datos";
            txtFolio.requestFocus();
        } else if (!validar.isNumber(txtFolio.getText())){
            resultado = "El folio debe ser un número";
            txtFolio.requestFocus();
        } 
        if (!(txtTelefonoR.getText().length() == 10 || txtTelefonoR.getText().length() == 7)) {
            resultado = "Debe ingresar un telefono fijo o celular, no ingreses carácteres entre los números";
            txtTelefonoR.requestFocus();
        }  
        if (!validar.noVacio(txtNombreR.getText())) {
            resultado = "Faltan datos";
            txtNombreR.requestFocus();
        } else if (txtNombreR.getText().length() > 100) {
            resultado = "El nombre de referencia es demaciado largo (máximo 100 carácteres";
        }
        if (txtTFFijo.getText().length() != 7) {
            resultado = "Debe ingresar un telefono fijo, no ingreses carácteres entre los números";
            txtTFFijo.requestFocus();
        }   
        if (txtCelular.getText().length() != 10) {
            resultado = "Debe ingresar un telefono celular, no ingreses carácteres entre los números";
            txtCelular.requestFocus();
        }   
        if (!validar.isEmail(txtCorreo.getText())) {
            resultado = "Ingresa un correo electrónico";
            txtCorreo.requestFocus();
        }
        if (!validar.noVacio(txtMunicipio.getText())) {
            resultado = "Faltan datos";
            txtMunicipio.requestFocus();
        } else if (txtMunicipio.getText().length() > 100) {
            resultado = "El municipio es demaciado largo (máximo 100 carácteres";
        }
        if (!validar.noVacio(txtColonia.getText())) {
            resultado = "Faltan datos";
            txtColonia.requestFocus();
        } else if (txtColonia.getText().length() > 100) {
            resultado = "La colonia es demaciado larga (máximo 100 carácteres";
        }
        if (!validar.noVacio(txtDireccion.getText())) {
            resultado = "Faltan datos";
            txtDireccion.requestFocus();
        } else if (txtDireccion.getText().length() > 100) {
            resultado = "La dirección es demaciado larga (máximo 100 carácteres";
        }
        if (!validar.noVacio(txtOcupacion.getText())) {
            resultado = "Faltan datos";
            txtOcupacion.requestFocus();
        } else if (txtOcupacion.getText().length() > 100) {
            resultado = "La ocupación es demaciado larga (máximo 100 carácteres";
        }
        if (!validar.noVacio(txtEdad.getText())) {
            resultado = "Faltan datos";
            txtEdad.requestFocus();
        } else if (!validar.isNumber(txtEdad.getText())){
            resultado = "La edad debe ser un número";
            txtEdad.requestFocus();
        }
        if (!validar.noVacio(txtNombre.getText())) {
            resultado = "Faltan datos";
            txtNombre.requestFocus();
        } else if (txtNombre.getText().length() > 100) {
            resultado = "El nombre es demaciado largo (máximo 100 carácteres";
        }
        if (!validar.esFecha(txtFecha.getText()).equals("true")){
            resultado = validar.esFecha(txtFecha.getText());
            txtFecha.requestFocus();
        }
        
        return resultado;
    }   
    
    private void llenarCajas() {
        try {
            rs = animal.consultarId(getId());
            if (rs.next()) {
                Blob blob = rs.getBlob("foto");
                ImageIcon icon;
                if (blob ==null) {
                    Image foto = new ImageIcon("src/iconos/gallery.png").getImage();
                    setImg(foto);
                    icon = new ImageIcon(foto.getScaledInstance(150, 200, Image.SCALE_SMOOTH));
                } else {
                    byte[] data = blob.getBytes(1, (int) blob.length());
                    blob.free();
                    BufferedImage foto = ImageIO.read(new ByteArrayInputStream(data));
                    setImg(foto);
                    icon = new ImageIcon(foto.getScaledInstance(150, 200, Image.SCALE_SMOOTH));
                }
                lblFoto.setIcon(icon);
                setTitle("Adoptar - " + rs.getString("nombre"));
                lblNombre.setText(rs.getString("nombre"));
                setTitle("Adoptados - " + lblNombre.getText());
                txtFecha.setText(validar.cambiarFecha(rs.getString("fecha")));
            }
            
            rs = adoptar.consultarId(getId());
            if (rs.next()) {
                txtNombre.setText(rs.getString("nombre"));
                txtEdad.setText(rs.getString("edad"));
                txtOcupacion.setText(rs.getString("ocupacion"));
                txtDireccion.setText(rs.getString("direccion"));
                txtColonia.setText(rs.getString("colonia"));
                txtMunicipio.setText(rs.getString("municipio"));
                txtCorreo.setText(rs.getString("correo"));
                txtCelular.setText(rs.getString("celular"));
                txtTFFijo.setText(rs.getString("telefonoFijo"));
                txtNombreR.setText(rs.getString("nombreReferencia"));
                txtTelefonoR.setText(rs.getString("telefonoReferencia"));
                txtFolio.setText(rs.getString("folio"));
            }
        } catch (SQLException | IOException ex) {
            System.out.println("Error en jifModificarAdopcion.llenarCajas():\n" + ex.getMessage());
        }
    }
    
    private void modificaciones(boolean cambioCredencial, boolean cambioComprobante) {
        adoptar.setCelular(txtCelular.getText());
        adoptar.setColonia(txtColonia.getText());
        adoptar.setComprobanteDomicilio(this.rutaComprobante);
        adoptar.setCorreo(txtCorreo.getText());
        adoptar.setCredencial(this.rutaCredencial);
        adoptar.setDireccion(txtDireccion.getText());
        adoptar.setEdad(Integer.parseInt(txtEdad.getText()));
        adoptar.setFolio(Integer.parseInt(txtFolio.getText()));
        adoptar.setMunicipio(txtMunicipio.getText());
        adoptar.setNombre(txtNombre.getText());
        adoptar.setNombreReferencia(txtNombreR.getText());
        adoptar.setOcupacion(txtOcupacion.getText());
        adoptar.setTelefonoFijo(txtTFFijo.getText());
        adoptar.setTelefonoReferencia(txtTelefonoR.getText());
        adoptar.setTipoComprobante(this.tipoCo);
        adoptar.setTipoCredencial(this.tipoCe);
        if (adoptar.modificar(getId(), cambioCredencial, cambioComprobante).startsWith("Se modifico") && animal.enviarA(getId(), "Adoptados", validar.getFecha()).startsWith("Se envio")) {
            JOptionPane.showMessageDialog(this, "Cambios guardados");
        } else {
            JOptionPane.showMessageDialog(this, "Ocurrió un error al guardar los cambios");
        }
        dispose();
    }

