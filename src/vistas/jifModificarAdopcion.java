    private void btnComprobanteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnComprobanteActionPerformed
        // TODO add your handling code here:
        File archivoD;
        this.selector.setFileFilter(this.filtro);
        int respuesta = this.selector.showOpenDialog(this);
        archivoD = this.selector.getSelectedFile();
        if (archivoD == null || archivoD.getName().trim().equals("") || archivoD.length() > 1048576) {
            JOptionPane.showMessageDialog(this, "Olvidaste subir un comprobante","Archivo incorrecto, el archivo es demaciado pesado, máximo 1024 KB", JOptionPane.ERROR_MESSAGE);
        } else {
            this.rutaComprobante = archivoD.getAbsolutePath();
            try {
                this.tipoCo = Files.probeContentType(archivoD.toPath());
            } catch (IOException ex) {
                System.out.println("Error en jifModificarAdopcion.btnCredencial:\n" + ex.getMessage());
            }
            setCambioComprobante(true);
        }
    }//GEN-LAST:event_btnComprobanteActionPerformed

