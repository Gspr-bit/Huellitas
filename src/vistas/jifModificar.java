    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        if (cboUsuario.getSelectedItem().toString().equals(getSesionIniciada())) {
            JOptionPane.showMessageDialog(this, "No puedes eliminar a este usuario porque su sesión está abierta");
        } else {
            if (JOptionPane.showConfirmDialog(this, "¿Seguro que quieres eliminar a " + cboUsuario.getSelectedItem().toString() + "?") == 0) {
                System.out.println(admins.eliminar(cboUsuario.getSelectedItem().toString()));
                llenarUsuarios();
            }  
        }    
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        // TODO add your handling code here:
        if (validarCajas().equals("true")) {
            admins.setIdAdministrador(0);
            admins.setNombre(txtNombre.getText());
            admins.setPassword(txtPassword.getText());
            admins.setPrincipal(cbtPrincipal.isSelected());
            
            if (admins.agregar().startsWith("Se agrego")) {
                llenarUsuarios();
                mostrarPanel(false);
                JOptionPane.showMessageDialog(this, "Usuario agregado");                
            } else {
                JOptionPane.showMessageDialog(this, "Hubo un error al intentar agregar al usuario");
            }
        } else {
            JOptionPane.showMessageDialog(this, validarCajas());
        }
    }//GEN-LAST:event_btnAceptarActionPerformed

