    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        // TODO add your handling code here:
        if (validarCajas().equals("true")) {
            if (admin.login(txtUsuario.getText(), txtPassword.getText()).equals("true")) { 
                setCursor(new Cursor(Cursor.WAIT_CURSOR));
                MdiHuellitas pantallaPrincipal = new MdiHuellitas();
                pantallaPrincipal.iniciarSesion(txtUsuario.getText(), admin.esAdministrador(txtUsuario.getText()));
                pantallaPrincipal.setLocationRelativeTo(null);
                pantallaPrincipal.setVisible(true);                
                dispose();                
            } else {
                JOptionPane.showMessageDialog(this, admin.login(txtUsuario.getText(), txtPassword.getText()));
                switch (admin.login(txtUsuario.getText(), txtPassword.getText())) {
                    case "Contraseña incorrecta":
                        txtPassword.setText("");
                        txtPassword.requestFocus();
                        break;
                    case "Nombre de usuario incorrecto":
                        txtUsuario.setText("");
                        txtUsuario.requestFocus();
                        break;
                    default:
                        txtUsuario.setText("");
                        txtUsuario.requestFocus();
                        txtPassword.setText("");
                        break;
                }
            }        
        } else {
            JOptionPane.showMessageDialog(this, validarCajas());
        }
    }//GEN-LAST:event_btnAceptarActionPerformed

