    public String validarCajas() {
        if (validar.noVacio(txtUsuario.getText()) && validar.noVacio(txtPassword.getText())) {
             return "true";
        } else if (validar.noVacio(txtPassword.getText())) {
            txtUsuario.requestFocus(); 
            return "Falta nombre de usuario";                       
        } else if (validar.noVacio(txtUsuario.getText())) {
            txtPassword.requestFocus();
            return "Falta contraseña";            
        } else {
            txtUsuario.requestFocus(); 
            return "Ingresa tus datos";                      
        }
    }
    
    
